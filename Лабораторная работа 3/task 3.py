# TODO  Напишите функцию count_letters
def count_letters(raw_text: str) -> dict[str: int]:
    stats = dict()
    # проходим по всем символам в тексте
    for symbol in raw_text.lower():
        # проверяем является ли символ буквой
        if symbol.isalpha():
            if symbol in stats.keys():
                # если попавшийся символ есть в словаре как ключ, то увеличиваем счётчик
                stats[symbol] += 1
            else:
                # если символа нет, то добавляем его как ключ сразу со заначением 1
                stats[symbol] = 1

    return stats


