def calculate_frequency(stats_by_letters: dict[str: int]) -> dict[str: float]:
    frequencies = dict()

    # в задании не написано (!!!), но подразумевается, что частоту считаем только от букв в тексте, а не всех символов
    # поэтому здесь создаётся список из всех значений словаря и вычисляется сумма от него
    text_length = sum([k for k in stats_by_letters.values()])

    # тут берём пары (ключ, значение) и просто считаем
    for cur_letter, letter_count in stats_by_letters.items():
        frequencies[cur_letter] = letter_count / text_length

    return frequencies


